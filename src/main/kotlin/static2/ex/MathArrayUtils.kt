package static2.ex

object MathArrayUtils {

    fun sum(array: Array<Int>): Int {
        return array.sum()
    }

    fun average(array: Array<Int>): Double {
        return array.average()
    }

    fun min(array: Array<Int>): Int {
        return array.min()
    }

    fun max(array: Array<Int>): Int {
        return array.max()
    }

}

